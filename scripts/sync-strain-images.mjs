// Regenerates src/lib/strain-images.ts from the files in public/strains/.
//
// Run after dropping generated atmospheric images into public/strains/:
//   npm run images:sync
//
// Every file named <slug>.<ext> (webp/avif/jpg/jpeg/png) becomes one
// entry in the manifest. The slug must match strainSlug(name) — e.g.
// GG4 → gg4.webp, "King Louis XIII" → king-louis-xiii.webp. The catalog
// cards read the manifest and render the photo over the family gradient
// when an entry exists.

import { readdirSync, writeFileSync, existsSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const dir = join(root, "public", "strains");

let files = [];
if (existsSync(dir)) {
  files = readdirSync(dir);
}

const entries = files
  .filter((f) => /\.(webp|avif|jpe?g|png)$/i.test(f))
  .map((f) => {
    const slug = f.replace(/\.[^.]+$/, "");
    return [slug, `/strains/${f}`];
  })
  .sort((a, b) => a[0].localeCompare(b[0]));

const body =
  entries.length === 0
    ? "  // populated by the sync script once images land in public/strains/"
    : entries
        .map(([slug, path]) => `  ${JSON.stringify(slug)}: ${JSON.stringify(path)},`)
        .join("\n");

const out = `// AUTO-GENERATED by scripts/sync-strain-images.mjs — do not edit by hand.
//
// Manifest of strains that have a generated atmospheric image committed to
// /public/strains. Card components look up by slug: when a path exists the
// card renders the photo over the family gradient; when it doesn't, the
// gradient stands alone (graceful fallback, no broken images).
//
// Workflow: drop <slug>.webp files into public/strains/, then run
//   npm run images:sync
// to regenerate this file. Commit both the images and this manifest.
export const STRAIN_IMAGES: Record<string, string> = {
${body}
};

export function strainImageBySlug(slug: string): string | null {
  return STRAIN_IMAGES[slug] ?? null;
}
`;

writeFileSync(join(root, "src", "lib", "strain-images.ts"), out);
console.log(`images:sync — wrote ${entries.length} image entr${entries.length === 1 ? "y" : "ies"}.`);
